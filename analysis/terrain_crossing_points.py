def rotate_point_180(point: tuple[float, float]) -> tuple[float, float]:
    """Rotate a map pixel around the exact image centre."""
    return MAP_WIDTH - 1 - point[0], MAP_HEIGHT - 1 - point[1]


def rotate_geometry_180(
    geometry: Sequence[tuple[float, float]],
) -> tuple[tuple[float, float], ...]:
    return tuple(rotate_point_180(point) for point in geometry)


def oriented_box(
    center: tuple[float, float], angle_deg: float, length: float, width: float,
) -> tuple[tuple[float, float], ...]:
    angle = math.radians(angle_deg)
    ux, uy = math.cos(angle), math.sin(angle)
    vx, vy = -uy, ux
    half_length, half_width = length / 2, width / 2
    cx, cy = center
    return tuple(
        (
            cx + along * half_length * ux + across * half_width * vx,
            cy + along * half_length * uy + across * half_width * vy,
        )
        for along, across in ((-1, -1), (1, -1), (1, 1), (-1, 1))
    )


def polygon_center(points: Sequence[tuple[float, float]]) -> tuple[float, float]:
    return (
        sum(point[0] for point in points) / len(points),
        sum(point[1] for point in points) / len(points),
    )


def map_to_field(x_px: float, y_px: float) -> tuple[float, float]:
    """Match web/app.js: map is 28x17 m with 1 m vertical padding."""
    x_m = x_px / MAP_WIDTH * FIELD_WIDTH_M
    y_m = 16.0 - y_px / MAP_HEIGHT * MAP_HEIGHT_M
    return (
        max(0.0, min(FIELD_WIDTH_M, x_m)),
        max(0.0, min(FIELD_HEIGHT_M, y_m)),
    )


def field_to_map(x_m: float, y_m: float) -> tuple[float, float]:
    return (
        x_m / FIELD_WIDTH_M * MAP_WIDTH,
        (16.0 - y_m) / MAP_HEIGHT_M * MAP_HEIGHT,
    )


