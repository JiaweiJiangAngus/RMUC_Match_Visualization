def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--db", type=Path, default=DEFAULT_DB)
    parser.add_argument("--x", type=float, help="query field x in metres")
    parser.add_argument("--y", type=float, help="query field y in metres")
    parser.add_argument("--padding", type=float, default=0.30)
    parser.add_argument("--prev-x", type=float)
    parser.add_argument("--prev-y", type=float)
    parser.add_argument("--capability", action="append", default=[])
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    features = build_features()
    paths = write_outputs(features, event_counts(args.db))
    for path in paths:
        print(path)

    query_values = (args.x, args.y)
    if (args.x is None) != (args.y is None):
        raise SystemExit("--x and --y must be supplied together")
    transition_values = (args.prev_x, args.prev_y, args.x, args.y)
    supplied = [value is not None for value in transition_values]
    if any(supplied) and not all(supplied) and args.prev_x is not None:
        raise SystemExit("--prev-x, --prev-y, --x and --y must be supplied together")
    if all(value is not None for value in transition_values):
        result = classify_transition(*transition_values, capabilities=args.capability, features=features)
        print(json.dumps(result, ensure_ascii=False, indent=2))
    elif all(value is not None for value in query_values):
        result = detect_features(args.x, args.y, features, args.padding)
        print(json.dumps(result, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
