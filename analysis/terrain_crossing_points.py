def point_in_polygon(x: float, y: float, polygon: Sequence[tuple[float, float]]) -> bool:
    inside = False
    previous = len(polygon) - 1
    for current, (current_x, current_y) in enumerate(polygon):
        previous_x, previous_y = polygon[previous]
        if (current_y > y) != (previous_y > y):
            at_x = (previous_x - current_x) * (y - current_y) / (previous_y - current_y) + current_x
            if x < at_x:
                inside = not inside
        previous = current
    return inside


def distance_to_segment(
    point: tuple[float, float], start: tuple[float, float], end: tuple[float, float],
) -> float:
    x, y = point
    sx, sy = start
    ex, ey = end
    dx, dy = ex - sx, ey - sy
    if dx == 0 and dy == 0:
        return math.hypot(x - sx, y - sy)
    ratio = max(0.0, min(1.0, ((x - sx) * dx + (y - sy) * dy) / (dx * dx + dy * dy)))
    return math.hypot(x - (sx + ratio * dx), y - (sy + ratio * dy))


def geometry_distance(
    point: tuple[float, float], geometry: Sequence[tuple[float, float]], geometry_type: str,
) -> float:
    if geometry_type == "polygon" and point_in_polygon(*point, geometry):
        return 0.0
    pairs = list(zip(geometry, geometry[1:]))
    if geometry_type == "polygon":
        pairs.append((geometry[-1], geometry[0]))
    return min(distance_to_segment(point, start, end) for start, end in pairs)


def _orientation(a: tuple[float, float], b: tuple[float, float], c: tuple[float, float]) -> float:
    return (b[0] - a[0]) * (c[1] - a[1]) - (b[1] - a[1]) * (c[0] - a[0])


def segments_intersect(
    a: tuple[float, float], b: tuple[float, float],
    c: tuple[float, float], d: tuple[float, float],
) -> bool:
    o1, o2 = _orientation(a, b, c), _orientation(a, b, d)
    o3, o4 = _orientation(c, d, a), _orientation(c, d, b)
    epsilon = 1e-9
    if abs(o1) < epsilon and distance_to_segment(c, a, b) < epsilon:
        return True
    if abs(o2) < epsilon and distance_to_segment(d, a, b) < epsilon:
        return True
    if abs(o3) < epsilon and distance_to_segment(a, c, d) < epsilon:
        return True
    if abs(o4) < epsilon and distance_to_segment(b, c, d) < epsilon:
        return True
    return (o1 > 0) != (o2 > 0) and (o3 > 0) != (o4 > 0)


def segment_hits_feature(
    start: tuple[float, float], end: tuple[float, float], feature: Feature,
) -> bool:
    geometry = feature.map_geometry_px
    if feature.geometry_type == "polygon" and (
        point_in_polygon(*start, geometry) or point_in_polygon(*end, geometry)
    ):
        return True
    pairs = list(zip(geometry, geometry[1:]))
    if feature.geometry_type == "polygon":
        pairs.append((geometry[-1], geometry[0]))
    return any(segments_intersect(start, end, a, b) for a, b in pairs)


def feature_to_dict(feature: Feature) -> dict:
    field_geometry = tuple(map_to_field(x, y) for x, y in feature.map_geometry_px)
    center_field = map_to_field(*feature.center_map_px)
    return {
        "feature_id": feature.feature_id,
        "category": feature.category,
        "category_zh": CATEGORY_ZH[feature.category],
        "side": feature.side,
        "kind": feature.kind,
        "geometry_type": feature.geometry_type,
        "gate_index": feature.gate_index,
        "source": feature.source,
        "symmetry": feature.symmetry,
        "default_traversability": feature.default_traversability,
        "capability_tag": feature.capability_tag,
        "height_difference_m": feature.height_difference_m,
        "physical_direction": "bidirectional" if feature.kind != "terrain_region" else None,
        "center_map_px": [round(value, 3) for value in feature.center_map_px],
        "center_field_m": [round(value, 4) for value in center_field],
        "map_geometry_px": [[round(x, 3), round(y, 3)] for x, y in feature.map_geometry_px],
        "field_geometry_m": [[round(x, 4), round(y, 4)] for x, y in field_geometry],
        "note": feature.note,
    }


def detect_features(
    x_m: float,
    y_m: float,
    features: Sequence[Feature] | None = None,
    padding_m: float = 0.30,
    kinds: Iterable[str] = ("crossing_gate", "conditional_ledge", "terrain_boundary"),
) -> list[dict]:
    """Return physical features near a telemetry point in field metres."""
    features = features or build_features()
    allowed_kinds = set(kinds)
    point_px = field_to_map(x_m, y_m)
    # Convert a field-metre tolerance to an isotropic map-pixel approximation.
    padding_px = padding_m * ((MAP_WIDTH / FIELD_WIDTH_M + MAP_HEIGHT / MAP_HEIGHT_M) / 2)
    result = []
    for feature in features:
        if feature.kind not in allowed_kinds:
            continue
        distance_px = geometry_distance(point_px, feature.map_geometry_px, feature.geometry_type)
        if distance_px <= padding_px:
            result.append(
                {
                    "feature_id": feature.feature_id,
                    "category": feature.category,
                    "category_zh": CATEGORY_ZH[feature.category],
                    "side": feature.side,
                    "kind": feature.kind,
                    "distance_m_approx": round(distance_px / (MAP_WIDTH / FIELD_WIDTH_M), 4),
                    "default_traversability": feature.default_traversability,
                    "capability_tag": feature.capability_tag,
                }
            )
    return sorted(result, key=lambda item: item["distance_m_approx"])


def classify_transition(
    previous_x_m: float,
    previous_y_m: float,
    x_m: float,
    y_m: float,
    capabilities: Iterable[str] = (),
    features: Sequence[Feature] | None = None,
) -> list[dict]:
    """Classify a movement segment that intersects a crossing gate or ledge."""
    features = features or build_features()
    capabilities = set(capabilities)
    start, end = field_to_map(previous_x_m, previous_y_m), field_to_map(x_m, y_m)
    hits = []
    for feature in features:
        if feature.kind not in ("crossing_gate", "conditional_ledge", "terrain_boundary"):
            continue
        if not segment_hits_feature(start, end, feature):
            continue
        if feature.kind == "conditional_ledge":
            allowed: bool | None = feature.capability_tag in capabilities
        else:
            allowed = None  # Vehicle-specific; learn/label this from observed teams.
        hits.append(
            {
                "feature_id": feature.feature_id,
                "category": feature.category,
                "category_zh": CATEGORY_ZH[feature.category],
                "side": feature.side,
                "allowed": allowed,
                "required_capability": feature.capability_tag,
                "reason": (
                    "400 mm ledge requires jump_capable"
                    if feature.kind == "conditional_ledge"
                    else "terrain boundary requires a designated gate or vehicle-specific capability"
                    if feature.kind == "terrain_boundary"
                    else "vehicle-specific physical interface"
                ),
            }
        )
    return hits


def event_counts(db_path: Path) -> dict[str, int]:
    if not db_path.exists():
        return {}
    connection = sqlite3.connect(f"file:{db_path}?mode=ro", uri=True)
    rows = connection.execute(
        "SELECT 类别, COUNT(*) FROM events WHERE 类别 IN ('过中央高地','台阶跨越','飞坡') GROUP BY 类别"
    ).fetchall()
    connection.close()
    return {str(category): int(count) for category, count in rows}


def font(path: Path, size: int) -> ImageFont.FreeTypeFont:
    return ImageFont.truetype(str(path), size=size, index=0)


def draw_centered_label(
    draw: ImageDraw.ImageDraw,
    center: tuple[float, float],
    text: str,
    text_font: ImageFont.FreeTypeFont,
    outline: tuple[int, int, int, int],
) -> None:
    box = draw.textbbox((0, 0), text, font=text_font, stroke_width=0)
    width, height = box[2] - box[0] + 16, box[3] - box[1] + 10
    x, y = center
    draw.rounded_rectangle(
        (x - width / 2, y - height / 2, x + width / 2, y + height / 2),
        radius=8,
        fill=(7, 13, 20, 235),
        outline=outline,
        width=3,
    )
    draw.text(
        (x - (box[2] - box[0]) / 2, y - (box[3] - box[1]) / 2 - box[1]),
        text,
        font=text_font,
        fill="white",
    )


def draw_dashed_polyline(
    draw: ImageDraw.ImageDraw,
    points: Sequence[tuple[float, float]],
    fill: tuple[int, int, int, int],
    width: int = 8,
    dash: float = 20,
    gap: float = 12,
) -> None:
    drawing = True
    remaining = dash
    for start, end in zip(points, points[1:]):
        dx, dy = end[0] - start[0], end[1] - start[1]
        length = math.hypot(dx, dy)
        if not length:
            continue
        position = 0.0
        while position < length:
            step = min(remaining, length - position)
            finish = position + step
            if drawing:
                a = (start[0] + dx * position / length, start[1] + dy * position / length)
                b = (start[0] + dx * finish / length, start[1] + dy * finish / length)
                draw.line((a, b), fill=fill, width=width)
            position = finish
            remaining -= step
            if remaining <= 1e-9:
                drawing = not drawing
                remaining = dash if drawing else gap


def render_map(features: Sequence[Feature], output_path: Path) -> None:
    source = Image.open(MAP_PATH).convert("RGBA")
    if source.size != (MAP_WIDTH, MAP_HEIGHT):
        raise ValueError(f"unexpected map dimensions: {source.size}")

    panel_height = 485
    canvas = Image.new("RGBA", (MAP_WIDTH, MAP_HEIGHT + panel_height), "#10161d")
    canvas.alpha_composite(source, (0, 0))
    overlay = Image.new("RGBA", source.size, (0, 0, 0, 0))
    draw = ImageDraw.Draw(overlay)

    for feature in features:
        points = [(round(x), round(y)) for x, y in feature.map_geometry_px]
        if feature.category == "central_highland_region":
            draw.polygon(points, fill=(171, 109, 255, 28), outline=(196, 151, 255, 150), width=3)
        elif feature.category == "trapezoid_highland_top":
            draw.polygon(points, fill=(255, 205, 77, 32), outline=(255, 210, 91, 175), width=4)

    for feature in features:
        if feature.kind == "conditional_ledge":
            draw_dashed_polyline(draw, feature.map_geometry_px, (255, 154, 61, 255))
        elif feature.kind == "terrain_boundary":
            draw_dashed_polyline(draw, feature.map_geometry_px, (255, 218, 73, 255), width=7)

    side_color = {"blue": (0, 180, 255, 105), "red": (255, 74, 93, 105)}
    side_outline = {"blue": (65, 202, 255, 255), "red": (255, 101, 116, 255)}
    for feature in features:
        if feature.kind != "crossing_gate":
            continue
        points = [(round(x), round(y)) for x, y in feature.map_geometry_px]
        draw.polygon(points, fill=side_color[feature.side], outline=side_outline[feature.side], width=5)

    canvas.alpha_composite(overlay, (0, 0))
    draw = ImageDraw.Draw(canvas)
    code_font = font(FONT_BOLD, 22)
    title_font = font(FONT_BOLD, 31)
    body_font = font(FONT_REGULAR, 23)
    note_font = font(FONT_REGULAR, 20)
    strong_font = font(FONT_BOLD, 22)

